package com.birutekno.bsoleh.model;

public class Month {
    private String ar;

    private String en;

    private String number;

    public String getAr ()
    {
        return ar;
    }

    public void setAr (String ar)
    {
        this.ar = ar;
    }

    public String getEn ()
    {
        return en;
    }

    public void setEn (String en)
    {
        this.en = en;
    }

    public String getNumber ()
    {
        return number;
    }

    public void setNumber (String number)
    {
        this.number = number;
    }
}
